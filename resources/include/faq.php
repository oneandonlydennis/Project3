    <h1>Veelgestelde vragen</h1> 
    <h3>Hoe train je je geheugen?</h3>
    <p>Je hersenen onthouden van alles en nog wat. Maar soms is het moeilijk om bepaalde info te absorberen. 
    Dit kan te maken hebben met een desinteresse van de stof, de moeilijkheidsgraad, en hoe wakker je bent. 
    Je kan je geheugen trainen om dit soort stof makkelijker tot je te nemen. Dat is vooral handig als je veel
    moet onthouden, zoals voor je studie. Ook is het slim om je geheugen te trainen als je merkt dat je geheugen
    minder wordt, zoals bijvoorbeeld bij ouderdom of bij hersenschade.</p>
    <h3>Ik kan toch niks onthouden. Heeft het zin?</h3>
    <p>Jazeker. Iedereen kan zijn of haar geheugen trainen, en er zijn geen uitzonderingen. De instap kan soms te
    hoog zijn voor je, zeker als je veel moeite hebt met onthouden door omstandighednen. Wat je kan proberen is, 
    zo rot als dat het klinkt, te beginnen met oefeningen voor kinderen. Er zijn verschillende tests gedaan waarin
    mensen van verschillende IQ-groepen gestest zijn voor en na het trainen van je geheugen voor een paar maanden, 
    en bij elke doelgroep was er een aanzienlijke groei te zien in het niveau van het geheugen.</p>
    <h3>Waar kan ik mijn geheugen trainen?</h3>
    <p>Er zijn veel verschillende boeken, spellen, en oefeningen die beschikbaar zijn. Wij hebben zelf ook een plek
    om je geheugen te trainen. Log in op onze site en ga naar onze game. Dit is een simpele geheugentraining die je 
    kan blijven oefenen!</p>
